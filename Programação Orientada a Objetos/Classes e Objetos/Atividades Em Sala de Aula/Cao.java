public class Cao {

    String nome;
    String raca;
    int idade;
    String porte;
    String tipoPelo;
    String sexo;
    boolean vacinado;

    public Cao () {


    }

    public Cao(String nome, String raca, int idade, String porte, String tipoPelo, String sexo, boolean vacinado) {
        this.nome = nome;
        this.raca = raca;
        this.idade = idade;
        this.porte = porte;
        this.tipoPelo = tipoPelo;
        this.sexo = sexo;
        this.vacinado = vacinado;

    }

}
